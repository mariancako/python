szam=int(input('Adj meg egy szamot'))
szam2=int(input('Adj meg megegy szamot'))
jel=input('Adj meg egy jelet')
if (jel=='+'):
    print('Eredmeny',szam+szam2)
elif(jel=='-'):
    print('Eredmeny',szam-szam2)
elif(jel=='*'):
    print('Eredmeny',szam*szam2)
elif(jel=='//'):
    print('Eredmeny',szam//szam2)
elif(jel=='%'):
    print('Eredmeny',szam%szam2 )

                            
